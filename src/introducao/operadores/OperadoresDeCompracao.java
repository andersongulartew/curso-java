package src.introducao.operadores;

public class OperadoresDeCompracao {
    public static void main(String[] args) {

        boolean dezMaiorQueVinte = 10 > 20;
        System.out.println(dezMaiorQueVinte);

        boolean dezMaiorIgualVinte = 10 >= 20;
        System.out.println(dezMaiorIgualVinte);

        boolean dezMenorQueVinte = 10 < 20;
        System.out.println(dezMenorQueVinte);


        System.out.println(5 == 5);
        System.out.println(5 != 5);


    }
}
